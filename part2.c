// Обчисліть суму та кількість тільки цілих від’ємних непарних чисел. При введенні числа 13 цикл завершується.

#include <stdio.h>

int main() {
    int totalNumbers, number, sum = 0, count = 0;

    // Введення загальної кількості чисел
    printf("Введіть загальну кількість чисел: ");
    scanf("%d", &totalNumbers);

    // Перевірка на позитивну кількість чисел
    if (totalNumbers <= 0) {
        printf("Кількість чисел повинна бути позитивною!\n");
        return 1;
    }

    // Цикл для введення чисел та обчислення
    for (int i = 1; i <= totalNumbers; i++) {
        printf("Введіть число #%d: ", i);
        scanf("%d", &number);

        if (number == 13) {
            printf("Введено число 13. Цикл завершено.\n");
            break;
        }

        if (number < 0 && number % 2 != 0) {
            sum += number;
            count++;
        }
    }

    // Виведення результату
    printf("Сума від’ємних непарних чисел: %d\n", sum);
    printf("Кількість від’ємних непарних чисел: %d\n", count);

    return 0;
}
