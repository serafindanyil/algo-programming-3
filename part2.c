// У програмі визначається і заповнюється випад- ковими числами одновимірний масив та підраховується кількість додатних чисел між мінімальним і максимальним елементами масиву. #include<iostream>
// using namespace std;

#include <stdio.h>
#include <stdlib.h>  // Для функцій rand() і srand()
#include <time.h>    // Для ініціалізації генератора випадкових чисел

int main() {
    int arr[14];  // Масив розміром 14
    int oddCount = 0;  // Лічильник непарних чисел

    // Ініціалізація генератора випадкових чисел
    srand(time(0));

    // Заповнення масиву випадковими числами від -30 до 30
    for (int i = 0; i < 14; i++) {
        arr[i] = (rand() % 61) - 30;  // Генеруємо число від -30 до 30
    }

    // Обчислення кількості непарних чисел
    for (int i = 0; i < 14; i++) {
        if (arr[i] % 2 != 0) {  // Перевірка на непарність
            oddCount++;
        }
    }

    // Виведення масиву на екран
    printf("Масив:\n");
    for (int i = 0; i < 14; i++) {
        printf("%d ", arr[i]);
    }

    // Виведення результату обчислення
    printf("\nКількість непарних чисел: %d\n", oddCount);

    return 0;
}

