// Ось приклад програми на мові С, яка створює масив цілих чисел розміром 7, вводить його елементи з клавіатури, обчислює суму квадратів від'ємних елементів і виводить результати:

#include <stdio.h>

int main() {
    int arr[7];  // Масив розміром 7
    int sumOfSquares = 0;

    // Введення елементів масиву з клавіатури
    printf("Введіть 7 цілих чисел:\n");
    for (int i = 0; i < 7; i++) {
        printf("Число #%d: ", i + 1);
        scanf("%d", &arr[i]);
    }

    // Обчислення суми квадратів від'ємних елементів
    for (int i = 0; i < 7; i++) {
        if (arr[i] < 0) {
            sumOfSquares += arr[i] * arr[i];  // Квадрат від'ємного елемента
        }
    }

    // Виведення елементів масиву
    printf("\nВведені елементи масиву:\n");
    for (int i = 0; i < 7; i++) {
        printf("%d ", arr[i]);
    }

    // Виведення результату обчислень
    printf("\nСума квадратів від'ємних елементів: %d\n", sumOfSquares);

    return 0;
}
