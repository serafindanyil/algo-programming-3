// F=5а+b2; діапазон зміни а від 5 до 50 з кроком 5; діапазон зміни b від 4 до 10 з кроком 2.


#include <stdio.h>

int main() {
    int a, b;
    int f;

    // Зовнішній цикл для зміни значення а від 5 до 50 з кроком 5
    for (a = 5; a <= 50; a += 5) {
      
        for (b = 4; b <= 10; b += 2) {

            f = 5 * a + b * b;

            printf("a = %d, b = %d, Result = %d\n", a, b, f);
        }
    }

    return 0;
}
