// Визначте мінімальний непарний елемент масиву. Перший елемент масиву дорівнює 201. Розмір масиву 7.

#include <stdio.h>

int main() {
    int arr[7];  // Масив розміром 7
    int *ptr = arr;  // Покажчик на масив
    int minOdd = 0;  // Змінна для збереження мінімального непарного елемента
    int foundOdd = 0;  // Прапорець, чи було знайдено непарний елемент

    // Перший елемент масиву
    *ptr = 201;

    // Введення елементів масиву з клавіатури
    for (int i = 1; i < 7; i++) {
        printf("Введіть елемент #%d: ", i + 1);
        scanf("%d", ptr + i);  // Введення числа через покажчик
    }

    // Пошук мінімального непарного елемента
    for (int i = 0; i < 7; i++) {
        if (*(ptr + i) % 2 != 0) {  // Перевірка на непарність
            if (!foundOdd || *(ptr + i) < minOdd) {
                minOdd = *(ptr + i);
                foundOdd = 1;
            }
        }
    }

    // Виведення елементів масиву
    printf("\nЕлементи масиву:\n");
    for (int i = 0; i < 7; i++) {
        printf("%d ", *(ptr + i));  // Доступ до елементів через покажчик
    }

    // Виведення результату обчислення
    if (foundOdd) {
        printf("\nМінімальний непарний елемент: %d\n", minOdd);
    } else {
        printf("\nНепарні елементи не знайдені.\n");
    }

    return 0;
}
